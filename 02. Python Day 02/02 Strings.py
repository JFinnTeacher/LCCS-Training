#string slicing and indexing
testString = "Hello World"
print(testString[6:10]) # When indexing and slicing it stops one before the end (up to but not including)
print(testString[6:11])
print(testString[6:]) # prints everything after the index
print(testString[0:5])
print(testString[:5]) # prints everything up to the index from the start

print(len(testString))
print(testString[10])

